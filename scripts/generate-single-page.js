import fs from 'node:fs'
import path from 'node:path'

const files = [
  'privacy.md',
  'ai.md',
  'mobile.md',
  'audio.md',
  'beginners-guide.md',
  'developer-tools.md',
  'downloading.md',
  'educational.md',
  'file-tools.md',
  'gaming-tools.md',
  'gaming.md',
  'image-tools.md',
  'internet-tools.md',
  'linux-macos.md',
  'misc.md',
  'non-english.md',
  'reading.md',
  'social-media-tools.md',
  'storage.md',
  'system-tools.md',
  'text-tools.md',
  'torrenting.md',
  'unsafe.md',
  'video-tools.md',
  'video.md'
]

const OUTPUT_FILE = 'docs/public/single-page.md'

function generateSinglePage() {
  console.log('Generating single-page markdown...')

  let body =
    '\ufeff<!-- This is autogenerated content, do not edit manually. -->\n'

  const contents = files.map((file) => {
    const filePath = path.join('docs', file)
    if (!fs.existsSync(filePath)) {
      console.warn(`Warning: File ${filePath} not found.`)
      return ''
    }
    return fs.readFileSync(filePath, 'utf-8')
  })

  body += contents.join('\n\n')

  fs.writeFileSync(OUTPUT_FILE, body)
  console.log(`Successfully generated ${OUTPUT_FILE}`)
}

try {
  generateSinglePage()
} catch (error) {
  console.error('Error generating single-page:', error)
  process.exit(1)
}
